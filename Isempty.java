// Class Utama
public class isempty {
  public static void main(String[] args) {
    
    // Membuat Objek String
    String text1 = "";
    String text2 = "Hello, World";
      
    // Menampilkan Output
    System.out.println("Output pertama: " + text1.isEmpty());
    System.out.println("Output kedua: " + text2.isEmpty());
      
  }
}

// OUTPUT //
// Output pertama: true
// Output kedua: false